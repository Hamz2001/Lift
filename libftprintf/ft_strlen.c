#include "libftprintf.h"

size_t	ft_strlen(int n)
{
	size_t	i;

	i = 0;
	if (n < 0)
	{
		n *= -1;
		i++;
	}
	if (n == 0)
		return (1);
	while (n)
	{
		n = n / 10 ;
		i++;
	}
	return (i);
}
